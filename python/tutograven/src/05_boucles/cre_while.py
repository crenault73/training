# while: tant qu'une condition est vraie
# salarié 1500€/mois

salary = 1500

# Tant que le salaire < 2000€, +120€
while salary < 2000:
    # Ajouter 120€ au salaire
    salary += 120
    # Afficher le nouveau salaire
    print("Votre salaire actuel est de: {}€".format(salary))

print("Fin du programme")
